using namespace std;
#include <iostream>;

void main() {
	int count = 0;   //Creazione variabile contatrice
	for (int i = 0; i < 10; i++) {    //ciclo
		count++;                      //incrementazione della variabile contatore
		cout << "Variabile contatore"<< count << endl;  //stampa della variabile contatore
	}
}